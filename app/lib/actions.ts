'use server';
import { z} from 'zod';
import {revalidatePath} from "next/cache";
import {redirect} from "next/navigation";
import postgres from "postgres";

import {signIn} from "@/auth";
import {AuthError} from "next-auth";

const sql = postgres(process.env.POSTGRES_URL!, {ssl: "require"})


export type  State = {
    errors?: {
        customerId?: string[],
        amount?: string[],
        status?: string[],
    },
    message?: string | null
}

const FormSchema = z.object({
    id: z.string(),
    customerId: z.string({
        error: 'Please select a customer.'
    }),
    amount: z.coerce.number()
        .gt(0, {message: 'Please enter an amount greater than $0.'})
    ,
    status: z.enum(['pending', 'paid'], {
        error: 'Please select an invoice status.'
    }),
    date: z.string(),
})

const CreateInvoice = FormSchema.omit({id: true, date: true})


const UpdateInvoice = FormSchema.omit({id: true, date: true})

export async function createInvoice(preState: State, formData: FormData) {
    const validatedFields = CreateInvoice.safeParse({
        customerId: formData.get('customerId'),
        amount: formData.get('amount'),
        status: formData.get('status'),
    })

    if (!validatedFields.success) {
        return {
            errors: z.treeifyError(validatedFields.error).errors,
            message: 'Missing Fields. Failed to Create Invoice.',
        }
    }
    const {customerId, amount, status} = validatedFields.data;
    const amountInCents = amount * 100

    const date = new Date().toISOString().split('T')[0];

    try {
        await sql`
            INSERT INTO invoices (customer_id, amount, status, date)
            VALUES (${customerId}, ${amountInCents}, ${status}, ${date})
            ;
        `
    } catch (err) {
        console.error(err);
    }


    revalidatePath('/dashboard/invoices')
    redirect('/dashboard/invoices')
}


export async function updateInvoice(id: string, preState: State, formData: FormData) {
    const validateField = UpdateInvoice.safeParse({
        customerId: formData.get('customerId'),
        amount: formData.get('amount'),
        status: formData.get('status'),
    })

    if (!validateField.success) {
        return {
            errors: validateField.error.flatten().fieldErrors,
            message: 'Missing Fields. Failed to Update Invoice.',
        }
    }

    const {customerId, amount, status} = validateField.data;
    const amountInCents = amount * 100

    try {
        await sql`
            UPDATE invoices
            SET customer_id = ${customerId},
                amount      = ${amountInCents},
                status      = ${status}
            WHERE id = ${id};`
    } catch (err) {
        console.error(err);
    }

    revalidatePath('/dashboard/invoices')
    redirect('/dashboard/invoices')

}

export async function deleteInvoice(id: string) {
    await sql`DELETE
              FROM invoices
              WHERE id = ${id}`
    revalidatePath('/dashboard/invoices')
}


export async function authenticate(
    preState: string | undefined,
    formData: FormData
) {
    try {
        await signIn('credentials', formData)
    } catch (err) {
        if (err instanceof AuthError) {
            switch (err.type) {
                case "CredentialsSignin":
                    return "Invalid credentials."
                default:
                    return "Something went wrong."
            }
        }
        throw err;
    }
}

